
public class Livre {
int id;
String Titre;
String NomcompletAuteur;
String Categorie;
String Datedeedution;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getTitre() {
	return Titre;
}
public void setTitre(String titre) {
	Titre = titre;
}
public String getNomcompletAuteur() {
	return NomcompletAuteur;
}
public void setNomcompletAuteur(String nomcompletAuteur) {
	NomcompletAuteur = nomcompletAuteur;
}
public String getCategorie() {
	return Categorie;
}
public void setCategorie(String Categorie) {
	this.Categorie = Categorie;
}
public String getDatedeedution() {
	return Datedeedution;
}
public void setDatedeedution(String datedeedution) {
	Datedeedution = datedeedution;
}
@Override
public String toString() {
	return "Livre [id=" + id + ", Titre=" + Titre + ", NomcompletAuteur=" + NomcompletAuteur + ", categorie="
			+ Categorie + ", Datedeedution=" + Datedeedution + "]";
}

}




 
 
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 

